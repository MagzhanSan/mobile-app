// Функция для получения перевода
export const getTranslation = (
  key: keyof typeof TRANSLATIONS,
  language: 'ru' | 'kk' = 'ru',
) => {
  const translation = TRANSLATIONS[key];
  if (!translation) {
    console.warn(`Translation not found for key: ${key}`);
    return key;
  }
  return translation[language];
};

// Функция для получения двуязычного сообщения
export const getBilingualMessage = (key: keyof typeof TRANSLATIONS) => {
  const ru = getTranslation(key, 'ru');
  const kk = getTranslation(key, 'kk');
  return `${ru}\n${kk}`;
};
